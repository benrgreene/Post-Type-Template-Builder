  <h1>Post Type Templates - Help</h1>
  <p>This plugin is meant to enable admins to tweak how single pages for posts (or pages) are displayed without having to mess with any code. This page will help walk through the basics of adding templates to your site.</p>

  <h2>Creating a Template</h2>
  <p>Simply click on 'Templates' > 'Add New Template' in the admin view. Then, title your template and fill out the content editor like you would a post. The only difference being, use the shortcode `[default-content]` where you want the content for each post to be.</p>
  <p>The templates accept anything that you'd put in a post: shortcodes, media, anything</p> 

  <h2>Adding a Template to a Post Type</h2>
  <p>For this, you simply go to the 'Templates' -> 'Set Templates' page in the admin view. There, you'll see a list of all available post types (and pages) that can take a template. Simply select the name of the template you want to use with that post type, and hit save.</p>
